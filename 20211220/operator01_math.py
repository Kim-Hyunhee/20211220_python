result01 = 3 + 6   # 사칙연산 가능
print('결과 1 : ', result01)

result02 = 12//5   # 나눗셈의 몫만 보고 싶을 때 활용
print('결과 2(나눗셈의 몫) : ',result02)

result03 = 12%5   #  나눗셈의 나머지만 보고 싶을 때 활용
print('결과 3(나눗셈의 나머지) :', result03)

result04 = result01 * result02   #  변수가 숫자형일 경우 변수끼리도 사칙연산 가능
print('결과 4 - 변수끼리의 계산 :' ,result04)

str1 = "안녕하세요."
str2 = '반갑습니다.'
result05 = str1 + str2
print('결과 5 - str간의 덧셈 : ', result05)

result06 = str1 * 3
print('결과 6 - str의 곱셈 :', result06)

result07 = (1 + 2) * 3
print('결과 7 - 여러 연산자의 조합 : ', result07)